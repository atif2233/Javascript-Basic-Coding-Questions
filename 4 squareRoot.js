// Math.sqrt() Method: Utilizes the built-in Math.sqrt() method to compute the square root.

var a = 9;
console.log(Math.sqrt(a)); // 3 
console.log(Math.sqrt(81)); // 9
console.log(Math.sqrt(26)); // give output in decimal because 26 is not a perfect square therefore it will give accurate value by calculating square root
console.log(Math.sqrt("hello")); // give output NaN -> not a number because alphabet is not consider as number 


